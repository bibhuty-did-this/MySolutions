package restart.dsa;

public class Bitmask {
    public static void main(String[] args) {
        System.out.println(1&20);
        System.out.println(1|20);
        System.out.println(~20);
        System.out.println(20<<1);
        System.out.println(20>>1);
        System.out.println(20^1);
    }
}
